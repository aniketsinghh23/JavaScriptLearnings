// Variables are names that hold data in memory.
// Use them when you want to store a value once and reuse it later.

let firstName = "Aman";
const age = 20;

// console.log() prints the current value to the screen.
console.log("My name is:", firstName);
console.log("My age is:", age);

// let can be updated after it is created.
firstName = "Sara";
console.log("Updated name:", firstName);

